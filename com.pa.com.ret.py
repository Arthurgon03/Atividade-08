def soma1(num1,num2):
    return (num1 + num2)

def soma2(num3,num4):
    return (num3 - num4)

resultado1 = soma1(2,4)
resultado2 = soma2(5,6)

resultado3 = (resultado1+resultado2)

print("A primeira conta é:",resultado1)
print("A segunda conta é:",resultado2)
print("A soma das duas contas é:",resultado3)
